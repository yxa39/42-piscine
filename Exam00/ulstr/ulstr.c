#include <unistd.h>

int	main(int argc, char **argv)
{
	char	s;

	if (argc == 2)
	{
		while (*argv[1])
		{
			if (*argv[1] >= 'a' && *argv[1] <= 'z')
			{
				s = *argv[1] + ('A' - 'a');
				write(1, &s, 1);
			}
			else if (*argv[1] >= 'A' && *argv[1] <= 'Z')
			{
				s = *argv[1] - ('A' - 'a');
				write(1, &s, 1);
			}
			else
				write(1, argv[1], 1);
			argv[1]++;
		}
	}
	write(1, "\n", 1);
}
